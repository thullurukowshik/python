def print_lower_triangle(matrix):
    # Complete this function
    i=0
    for row in matrix:
        print(row[:(i+1)])
        i+=1

def convert_string_to_int(list_a):
    new_list = []
    for item in list_a:
        num = int(item)
        new_list.append(num)
    return new_list


m, n = input().split()
m, n = int(m), int(n)
num_list = []

for i in range(m):
    list_a = input().split()
    list_a = convert_string_to_int(list_a)
    num_list.append(list_a)

print_lower_triangle(num_list)
